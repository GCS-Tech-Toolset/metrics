package com.gcs.metrics.cfg.properties;





public class MetricsConfigException extends Exception
{

	private static final long serialVersionUID = 1L;

	public MetricsConfigException()
	{
		super();

	}





	public MetricsConfigException(String message_, Throwable cause_, boolean enableSuppression_, boolean writableStackTrace_)
	{
		super(message_, cause_, enableSuppression_, writableStackTrace_);

	}





	public MetricsConfigException(String message_, Throwable cause_)
	{
		super(message_, cause_);

	}





	public MetricsConfigException(String message_)
	{
		super(message_);

	}





	public MetricsConfigException(Throwable cause_)
	{
		super(cause_);

	}



}
